    private final Lexeme lexeme;
    private final Multimap<Form, Word> expected;
    
    public VerbFormsTest (Word[] pp, Word[] expected) {
    	this.lexeme = lexemes.verb(pp);
    	
    	ImmutableMultimap.Builder<Form, Word> builder = new ImmutableMultimap.Builder<>();
    	for (Word word : expected) {
    		builder.put(word.getForm(), word);
    	}
    	
    	this.expected = builder.build();
    }
    
	@Test
	public void test() {
		for (Form form : allForms) {
			Collection<Word> result = lexeme.get(form);
			Collection<Word> expected = this.expected.get(form);
			
			if (result.size() != expected.size()) {
				showFailed (expected, result);
			}
			
			for (Word word : expected) {
				boolean found = false;
				
				for (Word x : result) {
					if (x.equals(word)) {
						found = true;
						break;
					}
				}
				
				if (!found) {
					showFailed (expected, result);
				}
			}
		}
	}
	
	private void showFailed (Collection<Word> wanted, Collection<Word> got) {
		StringBuilder sb = new StringBuilder();
		
		sb.append("\nWe wanted: [");
		for (Word word : wanted) {
			if (!sb.toString().isEmpty())
				sb.append(", ");
			
			if (word instanceof ParaphrasticWordImpl) {
				ParaphrasticWordImpl para = (ParaphrasticWordImpl) word;
				
				for (Word x : para.getArray()) {
					sb.append(x.toString() + " (" + x.getForm().toString() + ") ");
				}
			} else {
				sb.append(word.toString() + " (" + word.getClass().toString() + ")");
			}
		}
		sb.append("]\nWe got: [");
		for (Word word : got) {
			if (!sb.toString().isEmpty())
				sb.append(", ");
			
			if (word instanceof ParaphrasticWordImpl) {
				ParaphrasticWordImpl para = (ParaphrasticWordImpl) word;
				
				for (Word x : para.getArray()) {
					sb.append(x.toString() + " (" + x.getForm().toString() + ") ");
				}
			} else {
				sb.append(word.toString() + " (" + word.getClass().toString() + ")");
			}
		}
		sb.append("]");
		
		fail (sb.toString());
	}

